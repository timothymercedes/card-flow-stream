CREATE TABLE public.card_sets (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  category text NOT NULL DEFAULT 'pokemon',
  set_name text NOT NULL,
  set_code text,
  total integer NOT NULL DEFAULT 0,
  printed_total integer NOT NULL DEFAULT 0,
  series text,
  release_date date,
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (category, set_name)
);

GRANT SELECT ON public.card_sets TO anon;
GRANT SELECT ON public.card_sets TO authenticated;
GRANT ALL ON public.card_sets TO service_role;

ALTER TABLE public.card_sets ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Card sets are viewable by everyone"
ON public.card_sets FOR SELECT USING (true);

CREATE INDEX idx_card_sets_name_lower ON public.card_sets (lower(set_name));

